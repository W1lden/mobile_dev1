fun main() {
    val myInt: Int = 2024
    val myDouble: Double = 13.9
    val myString: String = "Asanali Bitanov"
    val myBoolean: Boolean = true

    println("Int: $myInt")
    println("Double: $myDouble")
    println("String: $myString")
    println("Boolean: $myBoolean")

    // Цикл for
    for (i in 1..10) {
        println(i)
    }

    // Цикл while
    var j = 1
    while (j <= 10) {
        println(j)
        j++
    }
}

